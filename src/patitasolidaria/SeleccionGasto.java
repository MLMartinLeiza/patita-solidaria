package patitasolidaria;

public class SeleccionGasto {
    String id;
    int indice;

    public SeleccionGasto(String id, int indice) {
        this.id = id;
        this.indice = indice;
    }

    public String getId() {
        return id;
    }

    public int getIndice() {
        return indice;
    }
    
    
}
